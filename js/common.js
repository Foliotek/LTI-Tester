$.fn.changeElementType = function(newType, content, setVal) {
	var attrs = {};

	$.each(this[0].attributes, function(idx, attr) {
		attrs[attr.nodeName] = attr.value;
	});
    
    var el = $("<" + newType + "/>", attrs).append(content).val(content);
	this.replaceWith(el);
    return el;
};
	

